// 2): QUERY middleware:
//     /^find/ => match any query method that starts with find (find, findOne, findOneAndUpdate, etc.).
tourSchema.pre(/^find/, function (next) {
  this.find({ secretTour: { $ne: true } });
  this.start = Date.now();
  next();
});

